farm = []

def get_plant_id(choice,x,y):
	if choice == "Default":
		return farmDefault[x][y]
	if choice == "Cactus":
		return farmCactus[x][y]
	if choice == "PumpkinOpt":
		return farmPumpkin[x][y]
	else:
		return farm[x][y]

def get_fertilizer_id(choice,x,y):
	if choice == "Default":
		return farmDefaultF[x][y]
	if choice == "Cactus":
		return farmCactusF[x][y]
	if choice == "PumpkinOpt":
		return farmPumpkinF[x][y]
	else:
		return farmF[x][y]

def plant_id(choice,x,y):
	if get_plant_id(choice,x,y) == "T":
		plant_tree()
	if get_plant_id(choice,x,y) == "C":
		plant_carrot()
	if get_plant_id(choice,x,y) == "P":
		plant_pumpkin()
	if get_plant_id(choice,x,y) == "A":
		plant_cactus()

def fertilize_id(choice,x,y):
	if get_fertilizer_id(choice,x,y) == 1:
		use_item(Items.Fertilizer)

def get_layout(choice):
	if choice == "Default":
		return farmDefault
	if choice == "Cactus":
		return farmCactus
	if choice == "PumpkinOpt":
		return farmPumpkin
	else:
		return farm

def find_Pumpkin_corners(FarmType, size):
	farmArray = get_layout(FarmType)
	corners = []
	for i in range(get_world_size()):
		for j in range(get_world_size()):
			if j == 0 and i == 0 and farmArray[i][j] == "P":
				corners += [[j,i],[j+size-1,i+size-1]]
			elif i == 0 and farmArray[i][j] == "P" and farmArray[i][j-1] != "P":
				corners += [[j,i],[j+size-1,i+size-1]]
			elif j == 0 and farmArray[i][j] == "P" and farmArray[i-1][j] != "P":
				corners += [[j,i],[j+size-1,i+size-1]]
			elif farmArray[i][j] == "P" and farmArray[i][j-1] != "P" and farmArray[i-1][j] != "P":
				corners += [[j,i],[j+size-1,i+size-1]]
	return corners

def partitioned_planter_pumpkin():
	init_x = get_pos_x()
	init_y = get_pos_y()
	y_slice = get_world_size()/(max_drones()-1)+1
	while True:
		for i in range(y_slice):
			for j in range(get_world_size()):
				if get_entity_type() == Entities.Dead_Pumpkin or (get_entity_type() != Entities.Pumpkin and can_harvest()):
						harvest()
				plant_id("PumpkinOpt",get_pos_x(),get_pos_y())
				fertilize_id("PumpkinOpt",get_pos_x(),get_pos_y())
				move(East)
			move(North)
		goto_coords(init_x,init_y)


